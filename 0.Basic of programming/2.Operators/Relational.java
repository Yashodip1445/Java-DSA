

public class Relational {
    public static void main(String[] args) {
        System.out.println("Relational ");

        int a = 10;
        int b = 5;
        // 1.a==b ==> false
        // 2.a!=b ==> true
        // 3.a>b ==> true
        // 4.a<b ==> false
        // 5.a<=b ==> false
        // 6. a>=b ==> true
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);

    }
}
